    private void btn_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimKiemActionPerformed
        String stringTimKiem = JOptionPane.showInputDialog(this, "Nhập vào tên khuyến mại để tìm kiếm:", "Tìm kiếm", JOptionPane.QUESTION_MESSAGE);
        List<KhuyenMai> listKhuyenMai = iDAO_KhuyenMai.findDataByName(stringTimKiem);
        DefaultTableModel defaultTableModel = (DefaultTableModel) tbl_QuanLyKhuyenMai.getModel();
        defaultTableModel.setRowCount(0);
        listKhuyenMai.stream().forEach((km) -> {
            defaultTableModel.addRow(new Object[]{km.getMaKhuyenMai(), km.getTenKhuyenMai(), km.getNoiDung(), km.getGiaTri(), km.getTrangThai() ? "Phục vụ" : "Không phục vụ"});
        });
        clear();
    }//GEN-LAST:event_btn_TimKiemActionPerformed

