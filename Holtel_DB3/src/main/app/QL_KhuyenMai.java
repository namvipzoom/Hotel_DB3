    private void load_KhuyenMai() {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tbl_QuanLyKhuyenMai.getModel();
        List<KhuyenMai> listKhuyenMai = new ArrayList<>(iDAO_KhuyenMai.getAllData());
        defaultTableModel.setRowCount(0);
        listKhuyenMai.stream().forEach((km) -> {
            defaultTableModel.addRow(new Object[]{km.getMaKhuyenMai(), km.getTenKhuyenMai(), km.getNoiDung(), km.getGiaTri(), km.getTrangThai() ? "Phục vụ" : "Không phục vụ"});
        });
    
    }

    private void clear() {
        txt_GiaTri.setText("0");
        txt_MaKhuyenMai.setText(null);
        txt_NoiDung.setText(null);
        txt_TenKhuyenMai.setText(null);
        check_PhucVu.setSelected(true);
        check_KhongPhucVu.setSelected(false);
        edit = false;
        btn_Sua.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/add_25px.png"))); // NOI18N
        btn_Sua.setText("Thêm mới");
    }

    private void details_KhuyenMai() {
        int selectedRow = tbl_QuanLyKhuyenMai.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) tbl_QuanLyKhuyenMai.getValueAt(selectedRow, 0);
            KhuyenMai khuyenMai = iDAO_KhuyenMai.findDataById(id);
            txt_GiaTri.setText(khuyenMai.getGiaTri() + "");

            txt_MaKhuyenMai.setText(khuyenMai.getMaKhuyenMai() + "");
            txt_NoiDung.setText(khuyenMai.getNoiDung() + "");
            txt_TenKhuyenMai.setText(khuyenMai.getTenKhuyenMai() + "");
            check_PhucVu.setSelected(true);
            if (khuyenMai.getTrangThai() == false) {
                check_KhongPhucVu.setSelected(true);
            }
            edit = true;
        }
        btn_Sua.setIcon(new javax.swing.ImageIcon(getClass().getResource("/image/update_25px.png"))); // NOI18N
        btn_Sua.setText("Cập nhật");
    }

