    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        int selectedRow = tbl_QuanLyKhuyenMai.getSelectedRow();
        int idDelete = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa không?", "Xác nhận xóa!", JOptionPane.OK_CANCEL_OPTION);
        if (idDelete == JOptionPane.OK_OPTION) {
            idDelete = (int) tbl_QuanLyKhuyenMai.getValueAt(selectedRow, 0);
            iDAO_KhuyenMai.deleteData(idDelete);
            load_KhuyenMai();
            clear();
        }
    }//GEN-LAST:event_btn_XoaActionPerformed


